
















operator = input('Enter an operator (+ - * / ) ')
num1 = float (input('enter a number ')) 
num2 = float (input('enter another number '))


if operator  == '-':
    print(num1 - num2)

elif operator == '+':
    print(num1 + num2)

elif operator == '*':
    print(num1 * num2)


elif operator == '/':
    print(num1 / num2)











else:
    print('you are banned from this program from now on')


    